import UIKit

public extension UIColor {
    
    static func weekNumWhiteColor() -> UIColor {
        return UIColor.white
    }
    
    static func weekNumMainColor() -> UIColor {
        return UIColor(red: 29/255.0, green: 177/255.0, blue: 214/255.0, alpha: 1.0)
    }
}
